#เครื่องหมายทางคณิต + - * / ( // คือหารแบบปัดเศษ) ( ** คือยกกำลัง) %(หารเอาเศษ) 

x=10
y=3

print("ผลบวก",x+y)
print("ผลลบ",x-y)
print("ผลคูณ",x*y)
print("ผลหาร",x/y)
print("ผลหารแบบปัดเศษ",x//y)
print("ยกกำลัง",x**y)
print("หารเอาเศษ",x%y) #เอาผลหารจากบรรทัด18มาทำต่อคือ10หาร3จะเหรือเศษ1คำตอบที่ได้เลยเป็น1