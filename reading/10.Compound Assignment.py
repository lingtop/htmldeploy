#Compound Assignment คือ ตัวดำเนินการที่ใช้ในการอัพเดตหรือแก้ไขค่าของตัวแปล 
#หรือเอาเครื่องหมาย = ไปรวมกับเครื่องหมายทางคณิตศาตร์

x = 10
print("ก่องเปลี่ยนแปลง x =",x)
# x = x+10 นี้คือการเขียนในรูปแบบธรรมดา
x+=10 #การเขียนในรูปแบบ  Compound Assignment คือเอาเท่ากับมารวมกับบวกเลยเป็นการเขียนแบบรวบรัดมากขึ้น
#สามารถใช้ = รวมได้ทั้ง x-=10, x*=10, x/=10, x//=10, x**2=10, x%=10 
print("หลังเปลี่ยนแปลง x =",x)